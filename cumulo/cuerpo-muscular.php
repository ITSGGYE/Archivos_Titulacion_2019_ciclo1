                        <h2>CONTRACTIBILIDAD</h2>
                        <p>La contractibilidad es la propiedad que tienen las fibras musculares para acortarse y hacerse más gruesas. Ello es posible porque cada célula contiene numerosos filamentos que están formados de dos proteínas diferentes llamadas actina y miosina, ambos tipos tienen aspecto diferente, los filamentos de actina son delgados y de color claro, mientras que los de miosina son de color oscuro y gruesos. Se alternan entre sí imbricados como cuando se entrelazan los dedos de las manos.Según el modelo del filamento deslizante, en situación de reposo la fibra muscular presenta un grado moderado de solapamiento entre los filamentos de actina y miosina, en estado de contracción el solapamiento aumenta, mientras que si se produce una elongación muscular el solapamiento disminuye y puede llegar a ser nulo.</p>
