                        <h2>CELULAS MUSCULARES</h2>
                        <p>Las células que forman el tejido muscular se llaman miocitos o fibras musculares debido a su forma alargada. Los miocitos del músculo estriado son muy largos, tienen forma cilíndrica y están multinucleados, es decir cada célula contiene varios núcleos. Las fibras musculares individuales se agrupan formando fascículos. Cada fibra está rodeada por una capa de tejido conjuntivo que se llama endomisio, mientras que el fascículo completo está envuelto en el perimisio. Varios fascículos se agrupan para formar el músculo integro que está rodeado por el epimisio.</p>
