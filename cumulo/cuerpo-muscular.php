                        <h2>PLACA NEUROMUSCULAR</h2>
                        <p>La placa neuromuscular o unión neuromuscular es la conexión que se establece entre una neurona motora y un músculo, mediante la cual la neurona transmite impulsos eléctricos a la fibra muscular y esta se contrae. En la placa neuromuscular intervienen dos células: la neurona motora (motoneurona) y la célula muscular (miocito). Entre ellas queda un espacio que se llama hendidura sináptica.Cuando un impulso nervioso (potencial de acción) viaja a través del axón de una neurona motora, alcanza al final de su recorrido la región que se conoce como botón terminal, en donde libera el neurotransmisor acetilcolina a la hendidura sináptica.</p>
