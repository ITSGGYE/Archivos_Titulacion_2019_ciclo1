                        <h2>MUSCULO ESTRIADO</h2>
                        <p>El músculo estriado es un tipo de músculo que tiene como unidad fundamental el sarcómero. Al verse a través de un microscopio, presenta estrías, que están formadas por las bandas claras y oscuras alternadas compuestas de actina y miosina. Está formado por fibras musculares en forma de huso, con extremos muy afinados, y más largas que las del músculo liso.La función principal de los músculos estriados es generar los movimientos voluntarios. Otra función es el mantenimiento de la estabilidad corporal que es posible gracias a una contracción parcial constante y mantenida que se llama tono muscular. Por otra parte los músculos estriados son la principal fuente de calor corporal y contribuyen a mantener la temperatura del organismo próxima a 37º centígrados..</p>
