                        <h2>ESQUELETO APENDICULAR</h2>
                        <p>Esqueleto apendicular: está formado por los huesos que forman la cintura escapular y cintura pelviana y los que constituyen las cuatro extremidades (brazos y piernas). Los huesos situados en las cinturas unen el esqueleto axial con el apendicular, es decir el tronco con las extremidades. La cintura escapular sirve de unión entre el tronco y la extremidad superior y la cintura pelviana une el tronco con la extremidad inferior, obsérvese que el concepto anatómico de cintura es diferente al uso que se le da a este término en la vida ordinaria.</p>
