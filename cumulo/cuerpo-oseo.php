                        <h2>TIPOS DE HUESOS</h2>
                        <p>Los huesos del esqueleto humano pueden dividirse en varios tipos dependiendo de su forma.</p>
                        <p>Huesos largos. Predomina la longitud sobre otras dimensiones como ocurre con los principales huesos de las extremidades: Fémur, tibia, peroné, húmero, cúbito y radio.</p>
                        <p>Huesos cortos. No predomina ninguna de las dimensiones sobre las restantes, su forma es más o menos cúbica. Por ejemplo los pequeños huesos que forman el carpo en la muñeca y el tarso en el tobillo</p>
                        <p>Huesos planos. Por ejemplo los huesos que forman la bóveda del cráneo.</p>
                        <p>Huesos irregulares. Por ejemplo las vértebras que forman la columna vertebral.</p>
