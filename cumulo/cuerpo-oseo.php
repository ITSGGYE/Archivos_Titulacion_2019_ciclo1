                        <h2>FUNCIONES DE LOS HUSOS</h2>
                        <p>El sistema esquelético tiene varias funciones:.</p>
                        <p>Sostén mecánico y mantenimiento postural: El esqueleto funciona como una estructura rígida que da forma al organismo, mantiene la morfología corporal y hace posible la posición bípeda</p>
                        <p>Movimiento: Las uniones entre dos huesos adyacentes (articulaciones) hacen posible los movimientos corporales, además los huesos sirven como lugar de inserción a los tendones de los músculos.</p>
                        <p>Protección: El esqueleto actúa en muchos casos como protección de los órganos internos. De esta forma los huesos que forman el cráneo protegen el encéfalo, las vértebras de la columna vertebral sirven de protección a la médula espinal y las costillas evitan que se produzcan daños en los pulmones, el corazón y los grandes vasos sanguíneos del tórax.</p>
