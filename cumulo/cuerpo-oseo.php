                        <h2>TEJIDO OSEO</h2>
                        <p>El tejido óseo es el que forma la mayor parte del esqueleto. Es un tejido dinámico que va modificándose a los largo de la vida y está formado por células y una matriz intercelular dura y resistente. La matriz ósea se compone de un 25% de agua, 25 % de proteínas y 50 % de sales minerales, principalmente sales de calcio (hidroxiapatita cálcica y fosfato cálcico). Las células que lo componen son de 4 tipos:</p>
                        <p>Células osteoprogenitoras. Son células precursoras que originan las restantes células del tejido óseo.</p>
                        <p>Osteoblastos. Son células formadoras de hueso, continuamente secretan moléculas de glucoproteínas, mucopolisacáridos y tropocolágeno que forman una matriz que se mineraliza mediante el depósito de sales de calcio.</p>
                        <p>Osteoclastos. Son células que degradan y reabsorben el hueso. Tienen por lo tanto la misión opuesta a los osteoblastos.</p>
