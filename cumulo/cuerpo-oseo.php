                        <h2>ESQUELETO HUMANO</h2>
                        <p>El esqueleto humano es el conjunto de huesos que proporciona al cuerpo humano su estructura. En el adulto consta de 206 - 208 (según diferentes autores)( aproximadamente 300 en los bebés) huesos articulados entre sí y estrechamente unidos a ligamentos, tendones, y músculos. Está formado por tejido óseo y tejido cartilaginoso. Representa alrededor del 12 % del peso total del cuerpo humano, por lo tanto el esqueleto de una persona de 75 kilogramos pesa 9 kilogramos.</p>
