                        <h2>ENFERMEDADES DEL SISTEMA ESQUELETICO</h2>
                        <p>Raquitismo. esta enfermedad es propia de la infancia se presenta con carencias de calcio y fósforo.</p>
                        <p>Fractura ósea. Consiste en la rotura de un hueso, generalmente causada por un traumatismo</p>
                        <p>Osteoporosis. Consiste en la perdida de densidad del hueso por deficiente mineralización y pérdida de las proteínas óseas. Provoca fragilidad de la estructura y se producen fracturas sin traumas o por traumatismos leves</p>
                        <p>Osteomielitis. Infección que afecta al tejido óseo. Uno de los gérmenes que puede provocar osteomielitis es el bacilo de Koch, agente causante de la tuberculosis.</p>
