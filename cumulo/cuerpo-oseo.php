                        <h2>ESTRUCTURAS DE LOS HUESOS</h2>
                        <p>Los huesos que forma el esqueleto constan de varias partes.</p>
                        <P>Diáfisis. Se llama diáfisis a la porción central o cuerpo de los huesos largos</P>
                        <p>Epífisis. Se llama epífisis a cada uno de los extremos de los huesos largos.</p>
                        <p>Metáfisis. Se llama metáfisis a una zona intermedia de los huesos largos que está situada entre la zona central (diáfisis) y los extremos (epífisis).</p>
                        <p>Cartílago articular. Es una estructura formada por tejido cartilaginoso que se interpone entre los extremos de dos huesos adyacentes, permitiendo el deslizamiento de las superficies en contacto gracias a que presenta un coeficiente de fricción muy bajo.</p>
                        <p>Periostio. Membrana de tejido conectivo fibrosa y resistente que cubre los huesos por su superficie externa.</p>
