                        <h2>HORMONAS ACTUAN SOBRE EL HUESO</h2>
                        <p>Hormona tiroidea. Estimula la osificación endocondral.</p>
                        <p>Hormona del crecimiento o GH. Aumenta la formación de hueso, su exceso causa gigantismo</p>
                        <p>Calcitonina. Disminuye la reabsorción ósea y baja el nivel de calcio en la sangre</p>
                        <p>Paratohormona. Acelera la reabsorción ósea y aumenta el calcio en la sangre</p>
                        <p>Estrógenos. En la mujer facilitan la remineralización y evitan la reabsorción de hueso</p>
                        <p>Andrógenos. Producen una aceleración del crecimiento óseo. Sin embargo el exceso de andrógenos en la etapa puberal causa también un cierre epifisario precoz por lo que la talla final adulta será más baja que la media</p>
                        
