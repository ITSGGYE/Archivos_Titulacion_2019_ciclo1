package com.example.veterinaria;

public class Cita {

    int idc;
    int idt;
    int hor;
    String fec;

    public int getIdc() {
        return idc;
    }

    public void setIdc(int idc) {
        this.idc = idc;
    }

    public int getIdt() {
        return idt;
    }

    public void setIdt(int idt) {
        this.idt = idt;
    }

    public int getHor() {
        return hor;
    }

    public void setHor(int hor) {
        this.hor = hor;
    }

    public String getFec() {
        return fec;
    }

    public void setFec(String fec) {
        this.fec = fec;
    }

    public Cita() {

    }

    public Cita(int idc, int idt,int hora, String fec) {
        this.idc = idc;
        this.idt = idt;
        this.hor = hora;
        this.fec = fec;
    }

}
